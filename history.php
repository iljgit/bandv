            <h1 style="padding-top: 0; margin-top: 0;">The History of our Allotment Society</h1>
<p class="large-text">
Like a lot of allotment sites nationwide, Burnside and Vinery Road have had their share of ups and downs. The last forty years or so in particular have seen a typical pattern of decline followed by an upsurge in interest, especially this side of the millennium. As a Society, we are fortunate in having good written records: there is a Vinery lease from the 1930s; accounts survive from the 1960s; and there is a volume of AGM minutes for 1980-98, painstakingly written by hand by Trevor Taylor (for whom the Pavilion at Vinery is named and who was for well over thirty years Secretary to the Society). Other information has been gleaned from local maps and other written records and from longstanding tenants.
</p>

<p><img src="assets/img/history/1926map.png" style="width: 100%" class="img img-responsive img-thumbnail"><br>
<i>OS Map dated 1926 showing Vinery Road and Burnside sites virtually touching</i>
</p>
<div style="display: inline-block">
<h1>Vinery Road site</h1>
<p class="large-text">
<img src="/assets/img/gallery/Harvest2017/11.jpg" class="img img-responsive img-thumbnail margin pull-left" style="width: 30%;"/>
Vinery Road Allotments were established in the mid-1920s: the purchase of land on Vinery Road for allotments on 7 July 1924 is noted in the Cambridge Corporation sanctions for loans for the purchase of land [Cambridgeshire Archives and Local Studies ref. CB/2/CL/13/16]. Historical maps give an idea of the development of the Vinery Road area. In 1885 part of the Vinery site was a slurry pit; there were no allotments and no Hemingford or Romsey Roads; Vinery Road was an avenue; and what was later to become Brooks Road was a tramway from the coprolite works. By 1901 Hemingford and Romsey Roads had been partly built. There were still no allotments but there was an eight-acre field. However, an extensive allotment site all the way to the tramway can be seen on the 1926 map shown here. By 1937 Brooks Road had been built but Vinery allotments still bordered onto it at that date.
</p>
</div>
<div style="display: inline-block">
<h1>Burnside site</h1>
<p class="large-text">
<img src="/assets/img/tday/2.jpg" class="img img-responsive img-thumbnail margin pull-right" style="width: 30%"/>
We know less about the Burnside site at the moment but, like Vinery Road, it appears on the 1926 map, almost adjacent to Vinery at that date. Burnside was originally known as Perne Road Allotments, which gives a good indication of their original extent. The two sites were originally so close to one another that this is presumably the reason why they came to be jointly administered by a single committee.
Don of Burnside remembered playing in the area as a child when there were tank traps there, presumably during or shortly after the Second World War. These must have been part of the GHQ (General Headquarters) line, otherwise known as the WWII Tank Trap, that ran across Queen Edith’s from south to north and was part of a much larger national defence system. Cambridge was one of the few settlements of any size to actually sit on the GHQ line. In the Burnside and Perne Road area it ran from what is now the junction of Ancaster Way and Tiverton Way to cut through the abandoned trackbed of the pre-1896 Newmarket railway at a point just west of Brookfields.
</p>
</div>

<h1>Loss of Land</h1>
<p>
<img src="/assets/img/history/2018map.jpg" style="width:100%;">
<br/><i>&copy; 2018 OpenStreetMap</i>
</p>
<p class="large-text">
Both Vinery and Burnside have decreased in size by at least half over the years. Vinery Road has shrunk well back from Brooks Road; and Burnside lost about half its land around the mid-1970s when the Tiverton Way estate was built.
In the 1980s, the threat to allotment land continued to increase because of competition for land in towns and cities. Nonetheless, the Society has fought tooth and nail to minimise the loss of land. For example:
<ul class="large-text">
<li>In 1980 there were proposals for a road through Burnside for Cambridge Folk Festival parking but the Society refused to give up the land;</li>
<li>St Philip’s School was built in 1984 on part of the Vinery Road site. Cambridge City Council originally asked Vinery to vacate twice the land it eventually relinquished, but the Society declined to give up this much and negotiated improvements to the site in return for the land;</li>
<li>In the mid-1980s there were proposals for a play area at Burnside for the Tiverton Way estate. This threat vanished for a time but, in 1989, when demand for allotments was in serious decline, the Society agreed to give up sixteen plots at Burnside for this facility. The Council did, however, agree by way of return to fence the area, realign the roadway and cut the grass on the site regularly.</li>
</ul>
</p>
<h1>Decline</h1>
<p class="large-text">
All this at a period of major decline in demand for allotments. From the late 1980s to the late 1990s, the situation appears to have been at its worst, particularly at Burnside, with many plots vacant or overgrown, and some being used effectively as dumping grounds or for commercial purposes. Nonetheless, the Society has remained in profit practically every year: losses are recorded only for a couple of years in the late 1980s because of low rents. The King’s seeds scheme, initiated by Trevor Taylor, probably in the late 1970s, may well have contributed something to the financial stability of the Society.
</p>
<div style="display: inline-block">
<h1>Revival</h1>
<p class="large-text">
<img src="/assets/img/vinery2018/D71_5381.jpg" class="img img-responsive img-thumbnail margin pull-left" style="width: 30%"/>
The beginning of the 21st century finally saw an upsurge in interest in allotments. Both sites are now flourishing and fully tenanted. There are lots of recent initiatives and new facilities, such as composting toilets, refreshment facilities, ‘Burn Bins’ (incinerators made by the Burnside site manager from Council waste bins to reduce the age-old complaints about bonfires), the Vinery pavilion, the Burnside orchard and picnic area, family and social activities, community projects and regular onsite projects undertaken by volunteers.
</p>
</div>
<h1>Membership</h1>
<p class="large-text">
And what of the membership? Some tenants have had amazingly long records of continuity. The AGM minutes for 1985 record the death of Mr E W Day, one of the then oldest members, who had joined in 1948. As for Trevor Taylor, he took on his plot at Vinery Road in about 1970 and did not cease active work on it until 2012. He retained a connection with the Society as Honorary President until his death in 2018. Contrary to popular notions, women members were certainly not unknown in earlier times, although it does appear that it was 1990 before any women members attended the AGM! The first women to join the Committee are recorded by the late 1990s.
</p>
<h1>And finally…</h1>
<p class="large-text">
… an example of the ability of allotments to excite opinion. The AGM minutes for 1980 record Government proposals to repeal some allotment legislation. A campaign against these proposals by the National Society resulted in more letters to politicians than the question of whether the Olympic Games should be held in Moscow!
</p>
<p class="text-right"><i>JBA Nov 2018</i></p>
