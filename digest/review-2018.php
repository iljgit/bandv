This year I also tried physalis, also known as cape gooseberry, a very sweet orange coloured fruit in a papery husk, related to the ornamental Chinese lanterns. The seeds took several weeks to germinate but they then made quick progress. The tiny yellow flowers with chocolate brown blotches in the middle were beautiful, and lots of fruit did form. The early ones ripened fully and tasted very nice, although they were quite small. However, a lot of the later fruits remained resolutely tiny and green. I had grown them in pots indoors and they grew very tall so quickly that they became too unwieldy to pot on into containers big enough to keep them adequately watered. That’s my excuse and I think it’s another experiment I’m unlikely to repeat!</p>
