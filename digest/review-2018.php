More recently, I had a go at growing melons, specifically Emir F1. They are supposed to be tolerant of low temperatures and the packet said it 
should be possible to grow them outside. They got off to a good start and I grew them on indoors. Once I’d worked out which flowers were the 
female ones that should produce fruit, it started to look quite promising, and lots of fruit appeared to be forming. 
</p><p class="large-text">However, I should probably have reduced the number of fruits considerably to give the remaining ones more chance of 
ripening. I then had to leave the plants to take their chances outside while I went on holiday. 
</p><p class="large-text">The final crop? It came to three melons not much bigger than a tennis ball. A sense of achievement, yes, and they did taste nice, but probably not worth all the time and effort!</p>
