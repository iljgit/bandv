<p class="large-text">Planning ahead for 2019, I have decided to try salsify and edamame beans. I am hoping these may both be a bit less testing than some of my experiments. Apparently, edamame beans don’t need staking, which suits me, although they do have a long growing season (I will bear this in mind in the light of my silly mistake with the oca…). Salsify is apparently a bit like a thin parsnip with a delicate flavour and easy to grow. Here’s hoping. I am also going to attempt pumpkins for the first time. I’m going straight in with Goosebumps, an ugly looking warty skinned affair. Watch this space – I will aim to report back in a year’s time!</p>
