The next year, I decided to try cucamelons. Kings describe these as ‘grape-sized cucumbers with a taste of water melon with a fresh tinge of lime’. Hmm… I’d say they don’t taste of much at all. They cropped prolifically enough and survived in pots outdoors, but I wouldn’t bother again, not least as I couldn’t persuade anyone else in the family to even try them!</p>
                </div>
