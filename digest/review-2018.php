<img class="img-responsive img-thumbnail pull-right margin" style="width: 30%;" src="annualimages/oxalis tuberosa by Frank Vincentz.jpg">
Moving on: this year (2018) I planted a few oca (oxalis tuberosa, grown widely in the Andes) which had been kindly given to me by a fellow Burnside tenant. It’s a tuber, usually reddish pink in colour, and can be used similarly to potatoes, although it has a slightly tangy lemon taste. It is blight resistant, which has got to be a good thing. Total disappointment, unfortunately: the plants grew well enough but didn’t produce any tubers at all. However, a couple of things might have gone wrong here. They do best where temperatures don’t exceed 80 degrees too often, so this summer’s heat and drought probably didn’t do them much good. Also, to be honest, I had no idea when to harvest them. I checked just now while writing this: apparently the tubers form during autumn and should be harvested mid-November. Oops!! Still a rookie… But I think this one is worth trying again…</p>
                </div>
