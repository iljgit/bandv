                    <img class="img-thumbnail img-responsive pull-left margin" style="width: 30%;" src="/community/images/as1.jpg"/>My name is Kate Lewis and I am an MSc Conservation student at the University College of London. For my dissertation topic I have chosen to study Cambridge allotments to ascertain how important they are as terrestrial habitat for amphibians and reptiles. I began my surveys in February 2016, surveying 15 sites across Cambridge, once a fortnight between 8-10am on a Saturday or Sunday. Unfortunately not all went to plan and a large number of my sites proved devoid of amphibians or reptiles. However, Burnside has been a consistent light at the end of the tunnel as it is absolutely bursting with frogs, toads and smooth newts and has been an absolute joy to survey. I also found a grass snake on one occasion and hope to find much more this year, including lizards and great crested newts! My initial observations of why Burnside is such a haven for amphibians is that there is a lot of scrub all over the site providing essential habitat, not just for the amphibians but also for their food source of various invertebrate species (unfortunately many allotments in Cambridge have taken a very ‘neat’ approach to maintaining their allotments, which is often very detrimental to our wildlife). No doubt the large, private lake and adjacent brook also play their part in making Burnside a very attractive home!
