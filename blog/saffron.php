To John Keats autumn was the “season of mists and mellow fruitfulness”. He wrote the well known Ode to Autumn in September, now more of a summer month 
due to global warming. On my allotment plot autumn is marked by the re-appearance of a crop that remains hidden below ground all summer and sparks 
into growth with the autumn rain. Nothing happens until that first soaking, making us regret that we didn’t take up our potatoes a week earlier! 
That valuable crop is saffron, whose green shoots appear in October followed by lilac flowers with subtle dark veining and elongated red stigma 
standing out against the petals. The colour comes from a compound called crocin or, to give it its full name, 8,8-diapo-8,8 caretonoic acid. 
Apologies to Keats for “unweaving the rainbow” - his criticism of Newton’s scientific explanation of light. To me the “unweaving” only makes it more 
fascinating and wondrous not less, but then I have a scientific eye not a poetic one. 
</p>
<p>
Unweaving it a bit more the flavour is due to 
picrocrocin (4-(β-D-glucopyranosyloxy)-2,6,6-trimethyl-1-cyclohexene-1-carboxaldehyde) and the wonderful aroma comes from safranal 
(2,6,6-trimethyl-1,3-cyclohexadiene-1-carboxaldehyde). Levels of those three compounds define the quality of saffron. To me that does nothing to 
undermine the wonder of a plant that is totally counter intuitive to temperate agriculture. It grows and flowers in autumn, stays green all winter and 
dies down over the summer. It’s a topsy-turvy kind of plant but of course the strategy is an ecological one designed to cope with a hot and dry summer and 
autumn rain. 
</p>
<p>
<img src="/assets/img/blog/saffron/saffron2.jpg" title="Saffron plants growing" alt="Saffron plants growing" class="img img-responsive img-thumbnail pull-right margin w-25" />
With global warming our climate is changing in favour of saffron culture with a hotter drier summer and milder winters. The first frost is later and 
winters are not as cold as I remember as a child. Here in East Anglia we have a historic tradition of saffron growing, most famously at Saffron Walden. 
Saffron was grown in the area from the middle ages until cheaper imports from Iran made UK labour intensive cultivation too expensive.  A sort of proto 
capitalist cautionary tale. Production will always move to a low cost environment with no allegiance to place or time! 
</p>
<p>
A saffron crocus still appears in the Saffron Walden coat of arms in recognition of its contribution to the wealth of the town. My own production is a 
little less ambitious but still provides us with our yearly requirement for saffron. At £75 a gram in the up market London stores it is a pretty useful 
crop to grow. 
</p>
<p>
The corms are readily available from UK suppliers and it is very easy to grow as it doesn’t need much looking after. The only difficult bit is 
harvesting. The pistils have to be removed individually every day as the flowers are just opening and they are easily battered down by rain. After picking 
they need to be dried at room temperature until they have a crisp texture to prevent fungal rot. They can then be stored almost indefinitely although they will 
lose flavour and odour with time.
</p>
