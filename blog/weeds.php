<h4>Weeds do at least prove that your soil is productive</h4>
<p>If weeds never grow on your plot it might indicate that your soil has problems. They can also tell you a bit about the type of soil you have and how it is working: horsetail, creeping buttercup and silverweed will grow where there is poor drainage; clover and vetch thrive on low nitrogen; and sorrel and plantain like acid soil. By contrast spurge, chickweed and groundsel like humus-rich, well aerated soil which is higher in nutrients.</p>

