                Now to the salsify. I got the seeds from Kings (they only sell one variety – ‘Sandwich Island’). 
                According to the RHS they are also known as ‘vegetable oyster’ on account of their delicate tasting tap root. 
                Kings describe them as being like a thin parsnip in appearance. They are certainly like a parsnip in that they have a long growing season. 
                I sowed them direct into the ground in early April and have just started harvesting them now (mid-October). 
                They didn’t need much attention at all (bonus). As for taste, however, some of my readers may shrink with horror when I say 
                I think they taste like a cross between parsnips and Jerusalem artichokes. They take quite some cooking too, definitely longer than parsnips. 
                I’m not convinced I will grow these again, but I plan to try scorzonera in 2020 – long roots with dark brown to black skins. 
                Again, Kings sell only one variety, ‘Long Black Maxima’, so I’ll try that. The catalogue says the growing method is identical to salsify, 
                but the taste is superior. Here’s hoping.
