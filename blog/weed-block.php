<h4>Step 1. Cut the weeds down.</h4>
<p>I use a rotary mower with the blade as low as possible, which will get very close to the ground and help guide where any bumps and dips are. Level any highs into the lows as much as you can. The flatter you can get it the better the end result. Grass cutting from nearby paths thrown down now will rot under the weed fabric you are about to install, be pulled down by the worms and improve the future soil fertility.
