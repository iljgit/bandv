<h3>Let’s get the bad news out of the way first</h3>
<p>At its simplest, you just don’t want weeds, the things that appear wherever they want, competing with your precious fruit and veg, the things you’ve chosen to sow and nurture. They compete for nutrients and water and, at certain times of year, they can grow faster than your seedlings and swamp them, especially slow to germinate seeds such as parsnips.</p>
<p>Weeding can be pretty hard work, and I won’t dwell at length on tools (every gardener swears by their favoured implement, be it hand fork, 
Dutch hoe, swan neck hoe, onion hoe – whatever sort you use it needs to be sharp). I find it rather satisfying to weed by hand, although this is best done in damp soil. 
Hoeing in drier conditions should be done by skimming along or just under the surface. Don’t hoe too deeply – this will just bring weed seeds nearer to the surface where they can germinate more easily.</p>
<p>Prioritise the workload and let some plants do the work for you, for example once cabbages and potatoes are well established, 
they will suppress most weeds. Concentrate on plants which won’t stifle weeds, such as onions. 
And weeding between rows but not within rows can at least reduce weeds substantially while saving some work.</p>
<p>As far as weedblocks are concerned, weed control fabric is the best thing (available at the <a href="/store.php">Store</a>, shameless plug) 
and there is a great article about how to use weedblock <a href="/tips/weed-block.php">here</a>. 
</p>
<p>
<b>Don’t use carpet as a weed suppressant!!  It is banned at both our sites.</b> It is effective but it all has to be taken up one day,
by which time it is invariably wet and heavy and very hard work to remove, and it can't be burnt.</p>


            </div>
        </div>

