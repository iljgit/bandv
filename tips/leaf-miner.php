<i>Allium</i> leaf miner, <i>Phytomyza gymnostoma</i>, is an insect pest of all <i>Allium</i> species; leeks, onions, chives, shallots and garlic. The larvae eat into the stems and bulbs of leeks, onions, chives and garlic and cause rotting by secondary bacterial infections. It is common in Europe and has been in the UK since 2002 where it was first found in the West Midlands. Since 2002 it has been slowly spreading using its limited flight capacity and by exportation with onions garlic and shallots.</p>
