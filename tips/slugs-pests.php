                        <h2>Deer, rabbits, mice and, sometimes at Burnside, ducks</h2>
                        <img src="/tips/images/rabbit.jpg" class="pull-right img-responsive img-thumbnail margin">
<p>Simply, fencing. Deer fencing should be quite high and robust, but it doesn’t need to be fine. The smaller the animal the finer the fencing should be. I’ve found that comprehensive blockading around the strawberry patch is necessary upon noticing little mouse runs, and bites.</p>

<p><emphasis>Please avoid anything that might cause unnecessary peril to animals, and remember that the allotments are regularly visited by foxes, hedgehogs, toads and frogs, and snakes (non-poisonous).</emphasis></p>
                       
