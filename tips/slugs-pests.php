                        <h2>Birds</h2>
                        <img src="/tips/images/pigeon.jpg" class="pull-left img-responsive img-thumbnail margin">
<p>The main bird to give trouble is the pigeon, or dove. As you might notice, they are a constant allotment feature. They will peck at many a crop, and ravage some of them. Weather conditions and seasons seem to determine how much they peck and what, but the main targets are peas – though broccoli can also get a hammering.</p>

<p>The most simple protection is diversion. Stringing up bits of cloth, shiny metal or old cds will deter most birds. Plastic bottles on sticks sometimes work, though they are not foolproof. A thorough matrix of string or wire can work, though it’s time-consuming.</p>

<p>I find the best option, which I always use for peas, is comprehensive netting. Make a cage. Blackbirds sometimes get in to have a peck around, but they don’t usually cause any trouble. They may indeed catch the odd slug or snail. Should you find a bird flapping around inside your netting or cage I suggest leaving it alone – it found its way in and, once you’ve gone, will find its way out. If it’s tangled up… well, that’s another matter. 
</p>
