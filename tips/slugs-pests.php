                        <h2>Slugs and Snails</h2>
                        <img src="/tips/images/slug.jpg" class="pull-right img-responsive img-thumbnail margin">
<p>
These are everywhere, though you will see them mostly during or after spring, summer and autumn rain. Virtuous people suggest you go out to the plot, night or day, pick the slimy creatures up and escort them off the premises. Of course, you should not throw them onto your neighbour’s plot, and there is the old tale that a thrown slug will simply return. I, when feeling virtuous, throw them over Cherry Hinton Brook, thinking that they can’t swim. However, if you’re not near the brook, that may be more difficult.</p>

<p>Other less squeamish people suggest going out (night or day) with a pair of scissors or a knife and dividing them into two. This can take time. Others recommend pouring salt on them, though a lingering death can haunt you. A large amount of people just stamp on them, which can be messy, or chuck them into a watery, and lidded, container. These tactics work, though you will still be plagued.</p>

<p>You can learn where they congregate – anywhere damp and dark and near to the tasty crop – and indeed, create shelters for them (stones, mats, bits of corrugated iron) so you’ll know where to find them. Some people try beer traps, though the beer might be better used.</p>

<p>Ultimately it can come down to the use of slug pellets, though this is a contentious issue for some. There is a wide range of slug pellets on the market. I, again hoping to be virtuous, have tried several non-toxic types but have found that most slugs just consider these an hors d’oeuvre.</p>

<p>Finally, there are the toxic blue pellets, as sold at the Burnside store. These do work, and in the morning you will see the evidence. However, caution is advised. A poisoned slug or snail will simply be a meal-in-waiting for many animals – birds and hedgehogs in particular – and they in turn will become poisoned should they eat the poisoned corpse. Hence, I go to some lengths to ensure no such animal can get to the corpse. Nets and barricades are used to prevent any entry onto the killing ground. This can be cumbersome, yet it should be effective, and you should have a harvestable crop.</p>

<p><b>Note</b>: the use of such pellets should only be used for the younger plants; once they become robust, the need for pellets should diminish.<br>
<b>Also note</b>: snails climb (sometimes way up into the foliage), slugs don’t (generally).<br>
<b>Further note</b>: slugs and snails will, should conditions dictate, go for anything, apart (maybe) from raspberries and kale.                        
</p>
