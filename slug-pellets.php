                <h2>Organic Slug Control</h2>
                <img src="/assets/img/blackbird.jpg" class="img-responsive img-rounded img-thumbnail pull-right" style="margin-left: 20px; margin-bottom: 20px;"/>

<p>
Although there aren’t any slug pellets that can be used in the organic garden it doesn’t mean that the garden will be completely overrun with slugs and snails, 
reducing all our plants to slimy stumps. Slug pellets aren’t very effective at controlling slugs. 
They will only kill around 10% of the slug and snail population in the average garden.
</p>
<p>
Organic garden management relies on prevention always being better than a cure. In the organic garden we use a system known as 
Integrated Pest Management Control sometimes referred to as IPM. IPM systems use a wide variety of controls to keep on top of pests and diseases. 
They depend upon the gardener taking steps to build up food chains within the garden that will attract suitable pest predators into the area.
</p>
<p>
<h3>Drainage</h3>

Moist conditions provide ideal conditions for slugs. The structure and drainage of the soil can be improved through the appropriate use of 
cultivation and by adding garden compost. Incorporating sharp sand or horticultural grit into poorly drained areas which can be identified as 
slug breeding sites can also be effective. The problem may be serious enough to require the installation of land drains to take the surplus water away.
</p>
<p>
<h3>Predatory insects</h3>

Providing the right habitat for natural enemies, especially ground beetles, will help to control slugs. 
Beetles, especially carabids are extremely effective at reducing slug populations and they feed voraciously on slug eggs, 
as well as the slugs themselves. Field margins and beetle banks provide ideal habitats for predatory beetles. It is worth mentioning that 
spring cultivation will reduce the beetle population, whilst autumn cultivation has little effect on their numbers.
</p>
<p>
<h3>Encourage birds</h3>

Many birds, particularly blackbirds and song thrushes, will eat considerable numbers of slugs and snails.  Providing bird boxes, winter feeding areas, 
trees and hedgerows, also any uncultivated areas, will all help to maintain and improve biodiversity. 
Hens are also effective at cleaning up an area both pre and post harvest.
</p>
