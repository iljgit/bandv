<p>
In the years between 2015 and 2019 the orchard has been largely cleared of weeds and sown with slow growing grass. It is mown regularly by a Burnside tenant on a voluntary basis. An attempt to grow small shrubs, climbers and wild flowers along the fence has had mixed results, competing as they do with the nettles.  
</p>
