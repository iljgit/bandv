<p>The idea grew in me after visiting the Histon and Impington Community Orchard (HICOP): surely we could do something like this at Burnside to support our sense of community. We could follow their example and buy only local heritage fruit trees. Working together on it could create a thing of beauty for passers-by along Snakey Path and could be a good advertisement for our allotment society. I suggested it to the committee expecting someone else to take it on. This is not what happens.  Anyone can propose an idea: if it is approved, the proposer works it up and leads on doing at least some of the work involved.  The committee agreed a budget for 17 trees, which would fit within the site we had available, an area next to the Burnside gate.  I then acquired a brochure from the East of England Apples and Orchards project (EEAOPS). I also went to the Botanical Gardens ‘Apple Day’ and put all the questions I could think of to the knowledgeable resource people there. 
</p>
<p>
From these two resources, and with much internal debate, I made a list of 14 trees, to include the following:
<ul>
<li>2 local plums (Swan and Willingham Gage);
<li>1 pear (Improved Fertility- it has been a disappointment so far); 
<li>2 cooking apples (Dr Harvey and Isaac Newton);
<li>8 eating apples (Lord Lambourne, Lady Hollendale, Chivers Delight, Murfitt’s Seedling, Thoday’s Quarrendon, New Rock Pippin, Huntingdon Codling and Suffolk Pink);
<li>1 russet (St Edmund’s).
</ul>
</p>
