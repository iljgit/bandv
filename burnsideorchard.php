<p>
In February 2019, after reviewing the progress of the trees, the committee decided to plant some more.  An order for 20 trees for Burnside and Vinery was delivered from EEAOPS: 14 for individual tenants to plant in their plots and 6 more for the orchard, as follows: 
<ul>
<li>1 plum (Cambridge Gage);
<li>1 pear (Beurre Bedford);
<li>2 cooking apples (Lane’s Prince Albert and Jolly Miller);
<li>2 eating apples (Morley’s Seedling and Wayside). 
</ul>
</p>
<p>
The orchard area was slightly extended, and the trees for the orchard were planted within 48 hours of delivery.  
These are excellent bare-rooted trees, already in bud, and looking very healthy. 
</p>
