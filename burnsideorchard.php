<p>
These are all on the MM106 dwarf rootstock.  We discussed these in committee, and three more were chosen and bought from a different nursery in Kent, as they were to be bigger trees, on the M25 rootstock, but also from East Anglian heritage stock. These are the Cottenham Seedling, Darcy Spice and another New Rock Pippin.  The idea was that these three trees would form a shady triangle for future picnics and a delightful place to rest in the shade.  The committee approved the plan and I ordered all 17 trees, which arrived on a chilly day in February 2015. Very soon the chair of the committee, the site managers and a team of volunteers planted them. We didn’t have time then to clear the ground of weeds and remnants of previous crops. 
</p>
