So here we are, in February 2019, with the old and new trees in bud, looking forward to blossom that will please the insects and bees and to fruit 
that will please us.  The orchard does indeed give interest and pleasure to those passing by: walkers often stop to chat and monitor the progress they observe.  
We are making a small contribution to sustaining the diversity of local heritage trees, not only through the community orchard but also through those 
that individual tenants have spread around both Burnside and Vinery.
