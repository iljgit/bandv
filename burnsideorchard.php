<p>
There was a significant crop from several trees in 2018 after the hot sunny summer. At the 2019 AGM we intend to discuss fair ways to share the crops amongst interested tenants.  A committee member has sourced permanent labels for the trees which record the tree variety, normal recommended picking time, and how long the produce can be expected to keep.  These will be put in beside the trees shortly to indicate this information to interested tenants who plan to engage in picking. 
</p>
